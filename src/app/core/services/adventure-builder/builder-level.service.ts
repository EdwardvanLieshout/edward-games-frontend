  private generateJumpzoneDrawables = (block: IBlock): IDrawable[] => {
    const drawables: IDrawable[] = [];
    for (let i = block.x; i < block.x + block.w; i += 100) {
      for (let j = block.y; j < block.y + block.h; j += 100) {
        drawables.push({
          id: block.id,
          x: i,
          y: j,
          w: 100,
          h: 100,
          distance: block.distance,
          name: block.name,
          animationCounter: 1,
        });
      }
    }
    return drawables;
  };
